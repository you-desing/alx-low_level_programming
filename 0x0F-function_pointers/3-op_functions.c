#include "3-calc.h"
/**
 * op_add - add function
 *
 * @a: first number
 * @b: second number
 *
 * Return: return the sum a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub function
 *
 * @a: first number
 * @b: second number
 *
 * Return: return the sub a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multplication function
 *
 * @a: first number
 * @b: second number
 *
 * Return: return the mult a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div function
 *
 * @a: first number
 * @b: second number
 *
 * Return: return the div a and b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod function
 *
 * @a: first number
 * @b: second number
 *
 * Return: return the mod a and b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
